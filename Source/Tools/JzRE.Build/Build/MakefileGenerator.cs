using System.Text;

namespace JzRE.Build;

/// <summary>
/// Generates a GNU Makefile for Linux/macOS CLI builds.
/// Called as a secondary artifact alongside IDE project files.
/// </summary>
public class MakefileGenerator
{
    private readonly BuildOptions _opts;
    private readonly string       _root;

    public MakefileGenerator(BuildOptions opts, string root)
    {
        _opts = opts;
        _root = root;
    }

    public void Generate(List<Module> modules)
    {
        var path = Path.Combine(_root, "Makefile");
        var sb   = new StringBuilder();

        var nativeModules = modules.Where(m => m.BuildNativeCode).ToList();
        var managedModules = modules.Where(m => m.BuildCSharp).ToList();

        sb.AppendLine("# Generated by JzRE.Build — do not edit");
        sb.AppendLine();
        sb.AppendLine("CONFIG ?= Debug");
        sb.AppendLine();
        sb.AppendLine(".PHONY: all clean generate");

        // Build target
        sb.AppendLine();
        sb.AppendLine("all:");
        foreach (var m in nativeModules)
            sb.AppendLine($"\t@echo [C++] {m.Name} && dotnet run --project Source/Tools/JzRE.Build -- --target {m.Name} --config $(CONFIG) --workspace $(CURDIR)");

        foreach (var m in managedModules)
            sb.AppendLine($"\t@echo [C#]  {m.Name} && dotnet run --project Source/Tools/JzRE.Build -- --target {m.Name} --config $(CONFIG) --workspace $(CURDIR)");

        // Clean
        sb.AppendLine();
        sb.AppendLine("clean:");
        sb.AppendLine("\trm -rf Binaries/ Cache/");
        sb.AppendLine();
        sb.AppendLine("generate:");
        sb.AppendLine("\tdotnet run --project Source/Tools/JzRE.Build -- --generate-project-files --workspace $(CURDIR)");

        File.WriteAllText(path, sb.ToString(), Encoding.UTF8);
        Console.WriteLine($"  Generated Makefile");
    }
}
