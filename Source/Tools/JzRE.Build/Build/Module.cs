namespace JzRE.Build;

/// <summary>
/// Base class for all JzRE build modules — mirrors FlaxEngine's Module pattern.
/// Each module has a corresponding *.Build.cs file in Source/ that subclasses this.
/// JzRE.Build discovers and compiles these at runtime to build the dependency graph.
/// </summary>
public abstract class Module
{
    public virtual string Name             => GetType().Name;
    public virtual string BinaryModuleName => Name;

    /// <summary>Whether this module has C++ source files to compile into a native library.</summary>
    public virtual bool BuildNativeCode => true;

    /// <summary>Whether this module has C# source files to compile.</summary>
    public virtual bool BuildCSharp => false;

    public List<string> PublicDependencies  { get; } = new();
    public List<string> PrivateDependencies { get; } = new();

    /// <summary>System libraries to link (e.g. "X11", "dl", "pthread"). Platform-conditional.</summary>
    public List<string> SystemLibraries { get; } = new();

    /// <summary>macOS frameworks to link (e.g. "Cocoa", "Metal"). Platform-conditional.</summary>
    public List<string> Frameworks { get; } = new();

    /// <summary>NuGet packages required for C# modules.</summary>
    public List<string> NuGetPackages { get; } = new();

    /// <summary>MSBuild OutputType for C# modules. Defaults to Library; set to WinExe for GUI executables.</summary>
    public virtual string CSharpOutputType => "Library";

    /// <summary>
    /// Whether this module has both native and managed code and should have
    /// bindings auto-generated by the header parser. When true, JzRE.Build
    /// parses C++ headers for API_* annotations and generates glue code
    /// before the compile step.
    /// </summary>
    public virtual bool HasBindings => BuildNativeCode && BuildCSharp;

    /// <summary>
    /// Absolute path to the directory containing this module's .Build.cs file.
    /// Set by Builder during discovery. Generated project files (.vcxproj, .csproj)
    /// and bindings are placed here.
    /// </summary>
    public string? ModuleDirectory { get; set; }

    /// <summary>
    /// Path to an existing project file (.csproj or .vcxproj) to reference in the
    /// generated solution instead of auto-generating one. Mirrors FlaxEngine's
    /// Target.CustomExternalProjectFilePath pattern for tool projects.
    /// </summary>
    public string? CustomExternalProjectFilePath { get; set; }

    /// <summary>Called after discovery to configure dependencies and compiler options.</summary>
    public virtual void Setup(BuildOptions options) { }
}
